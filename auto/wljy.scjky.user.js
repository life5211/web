    userInfoKey = `${login_name}_info`;
    subjectId = location.hash.substring(11);
    user = utils.localGet(userInfoKey, {scriptKcIds: [], state: 1});
    if (!user.playLog) user.playLog = {}
    if (!user.join_kcs) user.join_kcs = []
    doSubject();
    formInsert();
    showForm();
